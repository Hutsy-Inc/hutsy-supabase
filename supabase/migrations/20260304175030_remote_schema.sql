CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  meta jsonb;
begin
  meta := coalesce(new.raw_user_meta_data, '{}'::jsonb);

  insert into public.profiles (id, email, name, phone, zip, snapshot, consents, stripe_customer_id)
  values (
    new.id,
    new.email,
    nullif(new.email,''),
    nullif(meta->>'name',''),
    nullif(meta->>'phone',''),
    case when meta ? 'zip' then to_jsonb(meta->>'zip') else null end,
    case when meta ? 'snapshot' then meta->'snapshot' else null end,
    nullif(meta->>'stripe_customer_id','')
  )
  on conflict (id) do update
    set email  = excluded.email,
        name   = coalesce(excluded.name, profiles.name),
        phone  = coalesce(excluded.phone, profiles.phone),
        zip    = coalesce((excluded.snapshot->>''), profiles.zip), -- no-op safety
        snapshot = coalesce(excluded.snapshot, profiles.snapshot),
        consents = coalesce(excluded.consents, profiles.consents),
        stripe_customer_id = coalesce(excluded.stripe_customer_id, profiles.stripe_customer_id);

  return new;
end $function$
;

