        time_since_activation_seconds
      from public.nps_surveys
      where
        nps_sent_at >= (p_from::timestamp)
        and nps_sent_at <  ((p_to + 1)::timestamp)
    ),

    totals as (
      select
        count(*)::int as surveys_total,
        count(*) filter (where nps_sent_at is not null)::int as sent_total,
        count(*) filter (where score is not null)::int as responses_total,
        case
          when count(*) filter (where nps_sent_at is not null) = 0 then null
          else (count(*) filter (where score is not null)::numeric
               / count(*) filter (where nps_sent_at is not null)::numeric)
        end as response_rate,
        avg(score)::numeric as avg_score,
        case
          when count(*) filter (where score is not null) = 0 then null
          else (
            (count(*) filter (where score between 9 and 10)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
            -
            (count(*) filter (where score between 0 and 6)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
          )
        end as nps_score
      from base
    ),

    daily as (
      select
        date_trunc('day', nps_sent_at)::date as day,
        count(*)::int as sent_count,
        count(*) filter (where score is not null)::int as response_count,
        avg(score)::numeric as avg_score,
        case
          when count(*) filter (where score is not null) = 0 then null
          else (
            (count(*) filter (where score between 9 and 10)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
            -
            (count(*) filter (where score between 0 and 6)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
          )
        end as nps_score
      from base
      group by 1
      order by 1
    ),

    by_core_value as (
      select
        coalesce(first_core_value_type, 'unknown') as label,
        count(*)::int as sent_count,
        count(*) filter (where score is not null)::int as response_count,
        avg(score)::numeric as avg_score,
        case
          when count(*) filter (where score is not null) = 0 then null
          else (
            (count(*) filter (where score between 9 and 10)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
            -
            (count(*) filter (where score between 0 and 6)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
          )
        end as nps_score
      from base
      group by 1
      order by 2 desc   -- sent_count
    ),

    by_cohort as (
      select
        coalesce(activation_cohort::text, 'unknown') as label,
        count(*)::int as sent_count,
        count(*) filter (where score is not null)::int as response_count,
        avg(score)::numeric as avg_score,
        case
          when count(*) filter (where score is not null) = 0 then null
          else (
            (count(*) filter (where score between 9 and 10)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
            -
            (count(*) filter (where score between 0 and 6)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
          )
        end as nps_score
      from base
      group by 1
      order by 1 desc
    ),

    by_time_bucket_raw as (
      select
        case
          when time_since_activation_seconds is null then 'unknown'
          when time_since_activation_seconds < 3600 then '<1h'
          when time_since_activation_seconds < 86400 then '<24h'
          when time_since_activation_seconds < 3*86400 then '<3d'
          when time_since_activation_seconds < 7*86400 then '<7d'
          when time_since_activation_seconds < 14*86400 then '<14d'
          else '14d+'
        end as label,
        case
          when time_since_activation_seconds is null then 99
          when time_since_activation_seconds < 3600 then 1
          when time_since_activation_seconds < 86400 then 2
          when time_since_activation_seconds < 3*86400 then 3
          when time_since_activation_seconds < 7*86400 then 4
          when time_since_activation_seconds < 14*86400 then 5
          else 6
        end as sort_key,
        count(*) filter (where score is not null)::int as responses,
        avg(score)::numeric as avg_score,
        case
          when count(*) filter (where score is not null) = 0 then null
          else (
            (count(*) filter (where score between 9 and 10)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
            -
            (count(*) filter (where score between 0 and 6)::numeric
              / count(*) filter (where score is not null)::numeric) * 100
          )
        end as nps_score
      from base
      group by 1,2
    ),

    by_time_bucket as (
      select label, responses, avg_score, nps_score
      from by_time_bucket_raw
      order by sort_key
    ),

    recent_feedback as (
      select
        id,
        user_id,
        score,
        case
          when score between 9 and 10 then 'promoter'
          when score between 7 and 8 then 'passive'
          when score between 0 and 6 then 'detractor'
          else null
        end as tag,
        feedback,
        feedback_received_at,
        score_received_at,
        activation_cohort,
        first_core_value_type
      from base
      where feedback is not null and btrim(feedback) <> ''
      order by coalesce(feedback_received_at, score_received_at, nps_sent_at) desc
      limit greatest(p_feedback_limit, 0)
    )

    select jsonb_build_object(
      'totals', (select to_jsonb(t) from totals t),
      'daily',  (select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb) from daily t),
      'by_core_value', (select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb) from by_core_value t),
      'by_cohort',     (select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb) from by_cohort t),
      'by_time_bucket',(select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb) from by_time_bucket t),
      'recent_feedback',(select coalesce(jsonb_agg(to_jsonb(t)), '[]'::jsonb) from recent_feedback t)
    )
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION public.get_overview_kpis(p_from date, p_to date)
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  v_from_ts timestamptz := (p_from::timestamptz);
  v_to_ts   timestamptz := ((p_to + 1)::timestamptz); -- exclusive

  -- Meta spend
  v_ad_spend       numeric := 0; -- CAD total (existing behavior)
  v_ad_spend_usd   numeric := 0; -- NEW: converted to USD
  v_fx_missing_days int := 0;    -- NEW: if fx rows missing

  -- Revenue
  v_revenue numeric := 0;

  -- CAC
  v_new_paid int := 0;
  v_cac numeric := null;         -- CAD CAC (existing behavior)
  v_cac_usd numeric := null;     -- NEW: USD CAC (matches revenue currency)

  -- Paid start/end + churn
  v_paid_start int := 0;
  v_paid_end   int := 0;

  v_churned int := 0;
  v_churn_rate numeric := null;

  -- Chat totals
  v_total_msgs bigint := 0;
  v_user_msgs  bigint := 0;
  v_ai_msgs    bigint := 0;

begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  -- -----------------------------
  -- Ad spend in range (Meta Ads) - CAD (existing)
  -- -----------------------------
  select coalesce(sum(m.spend), 0)::numeric
  into v_ad_spend
  from public.meta_ads_daily m
  where m.day >= p_from and m.day <= p_to;

  -- -----------------------------
  -- Ad spend in range - USD (NEW)
  -- Uses fx_rates_daily.usd_per_cad
  -- Picks latest FX row on/before each spend day (handles weekends)
  -- -----------------------------
  select
    coalesce(sum(
      case
        when upper(coalesce(m.currency,'')) = 'USD' then m.spend
        when upper(coalesce(m.currency,'')) = 'CAD' then m.spend * fx.usd_per_cad
        else m.spend * fx.usd_per_cad -- fallback assume CAD if unknown
      end
    ), 0)::numeric,
    count(*) filter (where fx.usd_per_cad is null)::int
  into v_ad_spend_usd, v_fx_missing_days
  from public.meta_ads_daily m
  left join lateral (
    select r.usd_per_cad
    from public.fx_rates_daily r
    where r.day <= m.day
    order by r.day desc
    limit 1
  ) fx on true
  where m.day >= p_from and m.day <= p_to;

  -- If FX missing, v_ad_spend_usd will be lower than expected (or 0 for those days).
  -- We still return it + missing_days so UI can show a warning if needed.

  -- -----------------------------
  -- Revenue in range (PAID invoices)
  -- payments.amount is cents
  -- -----------------------------
  select coalesce(sum(p.amount), 0)::numeric / 100
  into v_revenue
  from public.payments p
  where p.status = 'paid'
    and p.created_at >= v_from_ts
    and p.created_at <  v_to_ts;

  -- -----------------------------
  -- New paid users in range
  -- = users whose FIRST-EVER paid payment is inside [from,to)
  -- -----------------------------
  with first_paid as (
    select
      p.user_id,
      min(p.created_at) as first_paid_at
    from public.payments p
    where p.status = 'paid'
      and p.user_id is not null
    group by 1
  )
  select count(*)::int
  into v_new_paid
  from first_paid fp
  where fp.first_paid_at >= v_from_ts
    and fp.first_paid_at <  v_to_ts;

  -- CAC (existing CAD)
  v_cac := case when v_new_paid > 0 then (v_ad_spend / v_new_paid::numeric) else null end;

  -- CAC in USD (NEW, correct vs USD revenue)
  v_cac_usd := case when v_new_paid > 0 then (v_ad_spend_usd / v_new_paid::numeric) else null end;

  -- -----------------------------
  -- Paid users START / END (best-effort)
  -- START = users with at least 1 paid payment before range
  -- END   = users currently active/trialing (current state)
  -- -----------------------------
  select count(*)::int
  into v_paid_start
  from (
    select distinct p.user_id
    from public.payments p
    where p.status = 'paid'
      and p.user_id is not null
      and p.created_at < v_from_ts
  ) x;

  select count(*)::int
  into v_paid_end
  from (
    select distinct s.user_id
    from public.subscriptions s
    where s.user_id is not null
      and s.stripe_status in ('active','trialing')
  ) y;

  -- -----------------------------
  -- Churned users in range (event-based)
  -- updated inside range AND now NOT paid
  -- and had a successful payment before that update
  -- -----------------------------
  select count(*)::int
  into v_churned
  from public.subscriptions s
  where s.user_id is not null
    and coalesce(s.updated_at, s.created_at) >= v_from_ts
    and coalesce(s.updated_at, s.created_at) <  v_to_ts
    and s.stripe_status not in ('active','trialing')
    and exists (
      select 1
      from public.payments p
      where p.user_id = s.user_id
        and p.status = 'paid'
        and p.created_at < coalesce(s.updated_at, s.created_at)
    );

  v_churn_rate := case when v_paid_start > 0 then (v_churned::numeric / v_paid_start::numeric) else null end;

  -- -----------------------------
  -- Chat totals in range
  -- -----------------------------
  select
    count(*)::bigint,
    count(*) filter (where m.role = 'user')::bigint,
    count(*) filter (where m.role <> 'user')::bigint
  into v_total_msgs, v_user_msgs, v_ai_msgs
  from public.chat_messages m
  where m.created_at >= v_from_ts
    and m.created_at <  v_to_ts;

  return jsonb_build_object(
    'range', jsonb_build_object('from', p_from, 'to', p_to),

    -- Existing keys (do not break)
    'ad_spend_range', v_ad_spend,         -- CAD
    'revenue_range',  v_revenue,          -- USD

    -- New FX-aware numbers
    'ad_spend_usd_range', v_ad_spend_usd,
    'fx', jsonb_build_object(
      'source', 'ECB',
      'missing_days', v_fx_missing_days
    ),

    'new_paid_users', v_new_paid,
    'cac',            v_cac,              -- CAD CAC (legacy)
    'cac_usd',        v_cac_usd,          -- USD CAC (correct vs revenue)

    'paid_users_start', v_paid_start,
    'paid_users_end',   v_paid_end,

    'churned_users', v_churned,
    'churn_rate',    v_churn_rate,

    'chat', jsonb_build_object(
      'total_msgs', v_total_msgs,
      'user_msgs',  v_user_msgs,
      'ai_msgs',    v_ai_msgs
    )
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION public.get_plaid_disconnect_queue(p_q text DEFAULT ''::text, p_limit integer DEFAULT 50, p_offset integer DEFAULT 0)
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
declare
  v_out jsonb;
begin
  -- Admin gate
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  with
  subs_latest as (
    select distinct on (s.user_id)
      s.user_id,
      lower(coalesce(s.stripe_status,'')) as stripe_status,
      coalesce(s.updated_at, s.created_at) as status_ts
    from public.subscriptions s
    where s.user_id is not null
    order by s.user_id, coalesce(s.updated_at, s.created_at) desc
  ),
  plaid_agg as (
    select
      pi.user_id,
      count(*)::int as item_count,
      max(pi.institution_name) as institution_name,
      max(pi.updated_at) as last_item_seen_at
    from public.plaid_items pi
    where pi.plaid_env = 'production'
      and pi.user_id is not null
    group by pi.user_id
  ),
  base as (
    select
      p.user_id,
      p.full_name,
      p.email,
      p.phone,

      p.bank_disconnected_at,

      sl.stripe_status,
      sl.status_ts,
      greatest(0, floor(extract(epoch from (now() - sl.status_ts)) / 86400))::int as days_nonpaid,

      pa.item_count,
      pa.institution_name,
      pa.last_item_seen_at
    from public.profiles p
    join plaid_agg pa on pa.user_id = p.user_id
    left join subs_latest sl on sl.user_id = p.user_id
    where
      p.bank_disconnected_at is null
      and coalesce(sl.stripe_status,'') not in ('active','trialing')
      and (
        p_q = ''
        or p.full_name ilike ('%'||p_q||'%')
        or p.email     ilike ('%'||p_q||'%')
        or p.phone     ilike ('%'||p_q||'%')
      )
  )
  select jsonb_build_object(
    'total', (select count(*) from base),
    'rows', coalesce((
      select jsonb_agg(to_jsonb(x) order by x.days_nonpaid desc, x.status_ts asc, x.user_id)
      from (
        select *
        from base
        limit p_limit
        offset p_offset
      ) x
    ), '[]'::jsonb)
  )
  into v_out;

  return v_out;
end;
$function$
;

CREATE OR REPLACE FUNCTION public.get_user_chat_history(p_user_id uuid, p_from date, p_to date, p_limit integer DEFAULT 500)
 RETURNS jsonb
 LANGUAGE plpgsql
AS $function$
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  return (
    select jsonb_build_object(
      'user_id', p_user_id,
      'from', p_from,
      'to', p_to,
      'rows', coalesce(jsonb_agg(to_jsonb(t) order by t.created_at asc), '[]'::jsonb)
    )
    from (
      select id, created_at, role, direction, body
      from public.chat_messages
      where user_id = p_user_id
        and created_at >= (p_from::timestamptz)
        and created_at <  ((p_to + 1)::timestamptz)
      order by created_at asc
      limit p_limit
    ) t
  );
end;
