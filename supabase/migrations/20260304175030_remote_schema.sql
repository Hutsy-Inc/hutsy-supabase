        select jsonb_agg(
          jsonb_build_object(
            'day', to_char(d.day,'YYYY-MM-DD'),

            'revenue', coalesce(rev.revenue,0),
            'active_users', coalesce(act.active_users,0),
            'active_subs', coalesce(active_subs.active_subs,0),

            'ad_spend', coalesce(ads.ad_spend,0),
            'impressions', coalesce(ads.impressions,0),
            'clicks', coalesce(ads.clicks,0),
            'reach', nullif(coalesce(ads.reach,0),0),

            'ctr', case
              when coalesce(ads.impressions,0) > 0 then (ads.clicks::numeric / ads.impressions)
