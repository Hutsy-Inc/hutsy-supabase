CREATE OR REPLACE FUNCTION public.get_chat_counts(p_user_ids uuid[], p_from date, p_to date)
 RETURNS TABLE(user_id uuid, total_msgs bigint, user_msgs bigint, ai_msgs bigint)
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  -- Admin gate (matches your dashboard security pattern)
  if not exists (
    select 1 from public.admins a where a.user_id = auth.uid()
  ) then
    raise exception 'not authorized';
  end if;

  return query
  select
    m.user_id,
    count(*)::bigint as total_msgs,
    count(*) filter (where m.role = 'user')::bigint as user_msgs,
    count(*) filter (where m.role <> 'user')::bigint as ai_msgs
  from public.chat_messages m
  where m.user_id = any(p_user_ids)
    and m.created_at >= (p_from::timestamptz)
    and m.created_at <  ((p_to + 1)::timestamptz)
  group by m.user_id;
end;
$function$
