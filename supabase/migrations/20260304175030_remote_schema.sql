CREATE OR REPLACE FUNCTION public.get_copilot_cohorts(p_basis text DEFAULT 'first_activity'::text, p_cohort_weeks integer DEFAULT 12, p_retention_weeks integer DEFAULT 8)
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  v_basis text := coalesce(nullif(p_basis,''), 'first_activity');
  v_cw int := greatest(coalesce(p_cohort_weeks, 12), 4);
  v_rw int := greatest(coalesce(p_retention_weeks, 8), 4);

  -- Use UTC-based “week starts” for consistency
  v_end_week date := date_trunc('week', (now() at time zone 'utc')::date)::date;
  v_start_week date := (v_end_week - ((v_cw - 1) * 7))::date;
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  return (
    with
    -- Weekly activity per user
    act_weekly as (
      select
        cad.user_id,
        date_trunc('week', cad.day)::date as week_start,
        sum(cad.event_count)::int as events,
        max(cad.last_active_at) as last_active_at
      from public.copilot_active_daily cad
      group by 1,2
    ),

    -- First-ever copilot activity day + week
    first_day as (
      select user_id, min(day) as first_day
      from public.copilot_active_daily
      group by 1
    ),
    first_week as (
      select
        fd.user_id,
        fd.first_day,
        date_trunc('week', fd.first_day)::date as first_week
      from first_day fd
    ),

    -- Cohort membership (basis selectable)
    cohort_users as (
      select
        p.user_id,
        date_trunc('week', p.created_at)::date as signup_week,
        fw.first_week,
        fw.first_day,
        case
          when v_basis = 'signup' then date_trunc('week', p.created_at)::date
          else fw.first_week
        end as cohort_week
      from public.profiles p
      left join first_week fw on fw.user_id = p.user_id
      where
        case
          when v_basis = 'signup'
            then date_trunc('week', p.created_at)::date between v_start_week and v_end_week
          else
            fw.first_week between v_start_week and v_end_week
        end
    ),

    cohort_sizes as (
      select cohort_week, count(*)::int as cohort_size
      from cohort_users
      group by 1
    ),

    -- For each cohort, count actives in each future week bucket (W0..Wn)
    retained as (
      select
        cu.cohort_week,
        aw.week_start,
        count(distinct aw.user_id)::int as active_users,
        coalesce(sum(aw.events),0)::int as total_events
      from cohort_users cu
      join act_weekly aw on aw.user_id = cu.user_id
      where aw.week_start between cu.cohort_week and (cu.cohort_week + ((v_rw - 1) * 7))::date
      group by 1,2
    ),

    grid as (
      select
        cs.cohort_week,
        cs.cohort_size,
        g.i as week_index,
        (cs.cohort_week + (g.i * 7))::date as week_start,
        coalesce(r.active_users,0) as active_users,
        coalesce(r.total_events,0) as total_events
      from cohort_sizes cs
      cross join generate_series(0, v_rw - 1) as g(i)
      left join retained r
        on r.cohort_week = cs.cohort_week
       and r.week_start  = (cs.cohort_week + (g.i * 7))::date
    ),

    cohort_rows as (
      select
        to_char(cohort_week, 'YYYY-MM-DD') as cohort_week,
        cohort_size,

        jsonb_agg(active_users order by week_index) as retained_users,

        jsonb_agg(
          case when cohort_size > 0
            then round((active_users::numeric / cohort_size) * 100, 1)
            else 0
          end
          order by week_index
        ) as retained_pct,

        jsonb_agg(
          case when active_users > 0
            then round((total_events::numeric / active_users), 2)
            else 0
          end
          order by week_index
        ) as avg_events_per_active

      from grid
      group by cohort_week, cohort_size
      order by cohort_week desc
    ),

    -- Weekly KPI series for the same time window
    weeks as (
      select generate_series(v_start_week, v_end_week, interval '7 days')::date as week_start
    ),
    wau as (
      select week_start,
             count(distinct user_id)::int as wau,
             coalesce(sum(events),0)::int as total_events
      from act_weekly
      where week_start between v_start_week and v_end_week
      group by 1
    ),
    new_users as (
      select w.week_start,
             count(distinct fw.user_id)::int as new_users
      from weeks w
      left join first_week fw on fw.first_week = w.week_start
      group by 1
    ),
    weekly as (
      select
        to_char(w.week_start,'YYYY-MM-DD') as week,
        coalesce(a.wau,0) as wau,
        coalesce(n.new_users,0) as new_users,
        greatest(coalesce(a.wau,0) - coalesce(n.new_users,0), 0) as returning_users,
        coalesce(a.total_events,0) as total_events,
        case when coalesce(a.wau,0) > 0 then round((a.total_events::numeric / a.wau), 2) else 0 end as avg_events_per_active
      from weeks w
      left join wau a on a.week_start = w.week_start
      left join new_users n on n.week_start = w.week_start
      order by w.week_start
    ),

    -- Lag: signup -> first copilot activity (only for users who have activity)
    lag_stats as (
      select
        round(avg(extract(epoch from (fw.first_day::timestamp - p.created_at)) / 86400)::numeric, 2) as avg_days,
        round(percentile_cont(0.5) within group (order by extract(epoch from (fw.first_day::timestamp - p.created_at)) / 86400)::numeric, 2) as median_days,
        round(percentile_cont(0.9) within group (order by extract(epoch from (fw.first_day::timestamp - p.created_at)) / 86400)::numeric, 2) as p90_days
      from public.profiles p
      join first_week fw on fw.user_id = p.user_id
      where p.created_at is not null
    )

    select jsonb_build_object(
      'settings', jsonb_build_object(
        'basis', v_basis,
        'cohort_weeks', v_cw,
        'retention_weeks', v_rw,
        'start_week', to_char(v_start_week,'YYYY-MM-DD'),
        'end_week', to_char(v_end_week,'YYYY-MM-DD')
      ),
      'weekly', coalesce((select jsonb_agg(to_jsonb(weekly)) from weekly), '[]'::jsonb),
      'cohorts', coalesce((select jsonb_agg(to_jsonb(cohort_rows)) from cohort_rows), '[]'::jsonb),
      'lag', (select to_jsonb(lag_stats) from lag_stats)
    )
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION public.get_copilot_day_users(p_day date, p_limit integer DEFAULT 200)
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  return (
    with rows as (
      select
        user_id,
        event_count::int as events,
        coalesce(last_active_at, first_active_at) as last_active_at,
        last_event,
        last_source
      from public.copilot_active_daily
      where day = p_day
