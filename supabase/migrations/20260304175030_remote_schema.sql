  -- Admin gate
  select exists(
    select 1 from public.admins a
    where a.user_id = v_uid
  ) into v_is_admin;

  if not v_is_admin then
    raise exception 'not authorized';
  end if;

  return (
    with base as (
      select
        id,
        user_id,
        activation_cohort,
        first_core_value_type,
        nps_sent_at,
        score,
        score_received_at,
        feedback,
