CREATE OR REPLACE FUNCTION public.get_copilot_kpis(p_from date, p_to date, p_top_limit integer DEFAULT 50)
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  return (
    with base as (
      select *
      from public.copilot_active_daily
      where day between p_from and p_to
    ),

    totals as (
      select
        count(distinct user_id)::int                              as active_users,
        coalesce(sum(event_count),0)::int                         as total_events,
        case when count(distinct user_id) > 0
          then round((sum(event_count)::numeric / count(distinct user_id))::numeric, 2)
          else 0
        end                                                       as avg_events_per_user
      from base
    ),

    daily as (
      select
        day,
        count(*)::int                         as active_users,     -- 1 row per user/day
        coalesce(sum(event_count),0)::int     as total_events
      from base
      group by day
      order by day
    ),

    last_row as (
      select distinct on (user_id)
        user_id,
        last_active_at,
        last_event,
        last_source
      from base
      order by user_id, last_active_at desc nulls last
    ),

    top_users as (
      select
        b.user_id,
        coalesce(sum(b.event_count),0)::int as events,
        lr.last_active_at,
        lr.last_event,
        lr.last_source
      from base b
      join last_row lr on lr.user_id = b.user_id
      group by b.user_id, lr.last_active_at, lr.last_event, lr.last_source
      order by events desc
      limit greatest(p_top_limit, 1)
    )

    select jsonb_build_object(
      'totals', (select to_jsonb(totals) from totals),
      'daily',  coalesce((select jsonb_agg(to_jsonb(daily) order by day) from daily), '[]'::jsonb),
      'top_users', coalesce((select jsonb_agg(to_jsonb(top_users) order by events desc) from top_users), '[]'::jsonb)
    )
  );
end;
$function$
;

CREATE OR REPLACE FUNCTION public.get_hutsy_dashboard()
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$declare
  v_total_users int;
  v_paid_users int;
  v_activated_users int;
  v_activation_rate numeric;
  v_revenue_30d numeric;
  v_ad_spend_30d numeric;
  v_roas_30d numeric;

  v_active_users_7d int;
  v_active_user_rate_7d numeric;
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'not authorized';
  end if;

  with
  subs_latest as (
    select distinct on (s.user_id)
      s.user_id,
      s.stripe_status,
      s.plan,
      s.interval,
      s.next_renewal,
      coalesce(s.updated_at, s.created_at) as last_seen_at
    from public.subscriptions s
    order by s.user_id, coalesce(s.updated_at, s.created_at) desc
  ),
  bank as (
    select pi.user_id, bool_or(pi.status = 'connected') as bank_connected
    from public.plaid_items pi
    group by pi.user_id
  ),
  credit as (
    select ai.user_id, (max(ai.last_user_token) is not null) as credit_connected
    from public.array_identities ai
    group by ai.user_id
  ),
  users as (
    select
      p.user_id,
      p.created_at,
      (sl.stripe_status in ('active','trialing')) as is_paid,
      coalesce(b.bank_connected,false) as bank_connected,
      coalesce(c.credit_connected,false) as credit_connected,
      (
        (sl.stripe_status in ('active','trialing'))
        and coalesce(b.bank_connected,false)
        and coalesce(c.credit_connected,false)
      ) as is_activated
    from public.profiles p
    left join subs_latest sl on sl.user_id = p.user_id
    left join bank b on b.user_id = p.user_id
    left join credit c on c.user_id = p.user_id
  )
  select
    (select count(*) from public.profiles),
    (select count(*) from (select distinct user_id from public.subscriptions where stripe_status in ('active','trialing')) x),
    (select count(*) from users where is_activated),
    (select coalesce(
      ((select count(*) from users where is_activated)::numeric / nullif((select count(*) from users where is_paid),0)),
      0
    )),
    (select coalesce(sum(pay.amount),0)::numeric/100
     from public.payments pay
     where pay.status = 'paid'
       and pay.created_at >= now() - interval '30 days'),
    (select coalesce(sum(m.spend),0)::numeric
     from public.meta_ads_daily m
     where m.day >= (current_date - 29))
  into v_total_users, v_paid_users, v_activated_users, v_activation_rate, v_revenue_30d, v_ad_spend_30d;

  -- Active users (7d): distinct users active in last 7 days (including today)
  select coalesce(count(distinct cad.user_id), 0)
  into v_active_users_7d
  from public.copilot_active_daily cad
  where cad.day >= (current_date - 6);

  v_active_user_rate_7d := coalesce(
    (v_active_users_7d::numeric / nullif(v_activated_users, 0)),
    0
  );

  v_roas_30d := case
    when coalesce(v_ad_spend_30d,0) > 0 then (v_revenue_30d / v_ad_spend_30d)
    else null
  end;

  return jsonb_build_object(
    'kpis', jsonb_build_object(
      'total_users', v_total_users,
      'paid_users', v_paid_users,
      'activated_users', v_activated_users,
      'activation_rate', v_activation_rate,

      'active_users_7d', v_active_users_7d,
      'active_user_rate_7d', v_active_user_rate_7d,

      'sub_revenue_30d', v_revenue_30d,
      'ad_spend_30d', v_ad_spend_30d,
      'roas_30d', v_roas_30d,
      'currency', 'USD'
