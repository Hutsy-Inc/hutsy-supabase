CREATE OR REPLACE FUNCTION public.bump_snapshot_feedback(choice text)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
declare
  v_uid uuid := auth.uid();
  v_day date := (now() at time zone 'utc')::date;
begin
  if v_uid is null then
    raise exception 'not authenticated';
  end if;

  insert into public.snapshot_feedback_daily(user_id, day, yes_count, no_count, last_choice, last_choice_at)
  values (
    v_uid,
    v_day,
    case when choice = 'yes' then 1 else 0 end,
    case when choice = 'no' then 1 else 0 end,
    choice,
    now()
  )
  on conflict (user_id, day)
  do update set
    yes_count = public.snapshot_feedback_daily.yes_count + (case when choice = 'yes' then 1 else 0 end),
    no_count  = public.snapshot_feedback_daily.no_count  + (case when choice = 'no'  then 1 else 0 end),
    last_choice = choice,
    last_choice_at = now(),
    updated_at = now();
end;
$function$
;

